// ----------------------------------------- \\\
// ---------------- IMPORTS ---------------- \\\
// ----------------------------------------- \\\
import $ from "jquery";



// ----------------------------------------- \\\
// ----------------- VARS ------------------ \\\
// ----------------------------------------- \\\



// ----------------------------------------- \\\
// ------------------ INIT ----------------- \\\
// ----------------------------------------- \\\
function init(_element){

    $(_element).on('focusout',function(){
        if($(this).find('input').val() == ''){
            $(this).removeClass('add-focus');
        }

        if($(this).find('textarea').val() == ''){
            $(this).removeClass('add-focus');
        }
    })

    $(_element).on('focusin',function(){
        $(this).addClass('add-focus');
    })

}


// ----------------------------------------- \\\
// ---------------- EXPORTS ---------------- \\\
// ----------------------------------------- \\\
export { init }

  
