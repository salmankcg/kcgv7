// ----------------------------------------- \\\
// ---------------- IMPORTS ---------------- \\\
// ----------------------------------------- \\\
import $ from "jquery";


// ----------------------------------------- \\\
// ----------------- VARS ------------------ \\\
// ----------------------------------------- \\\
var $cont       	= $('.modal');



// ----------------------------------------- \\\
// ------------------ INIT ----------------- \\\
// ----------------------------------------- \\\
function init(){
	setModal();
}



// ----------------------------------------- \\\
// ------------ PUBLIC FUNCIONS ------------ \\\
// ----------------------------------------- \\\
function resize(){}



// ----------------------------------------- \\\
// ------------ PRIVATE FUNCIONS ----------- \\\
// ----------------------------------------- \\\
function setModal(){

	if(localStorage.getItem('loaded') != 'yes'){
		setTimeout(function(){
			$cont.fadeIn(400).css('display','flex');
		},4000);

		localStorage.setItem('loaded', 'yes');
	}

	$cont.find('.button').on('click',function(){
		$cont.fadeOut(400);
	});

	$cont.find('.background').on('click',function(){
		$cont.fadeOut(400);
	});

}


// ----------------------------------------- \\\
// ---------------- EXPORTS ---------------- \\\
// ----------------------------------------- \\\
export { init, resize }
