// ----------------------------------------- \\\
// ---------------- IMPORTS ---------------- \\\
// ----------------------------------------- \\\
import $ from "jquery";
import 'slick-carousel';

// ----------------------------------------- \\\
// ----------------- VARS ------------------ \\\
// ----------------------------------------- \\\
var $cont       = $('.gallery');
var $slides      = $cont.find('.slides');




// ----------------------------------------- \\\
// ------------------ INIT ----------------- \\\
// ----------------------------------------- \\\
function init(){
    setGallery();
}


// ----------------------------------------- \\\
// ------------ PUBLIC FUNCIONS ------------ \\\
// ----------------------------------------- \\\
function resize(){}



// ----------------------------------------- \\\
// ------------ PRIVATE FUNCIONS ----------- \\\
// ----------------------------------------- \\\
function setGallery(){
    $slides.slick({
		arrows		    : false,
		dots		    : true,
		infinite	    : false,
		padding		    : '120px',
	});

    $cont.find('.t-prev').on('click',function(){
        $slides.slick('slickPrev');
    });

    $cont.find('.t-next').on('click',function(){
        $slides.slick('slickNext');
    });
}


// ----------------------------------------- \\\
// ---------------- EXPORTS ---------------- \\\
// ----------------------------------------- \\\
export { init, resize }